package ca.openosp.openo.ws.rest.to.model;

import java.io.Serializable;
import java.util.Date;

import javax.xml.bind.annotation.XmlRootElement;

@XmlRootElement
public class PharmacyInfoTo1 implements Serializable {

    private static final long serialVersionUID = 1L;

    private Integer id;
    private String name;
    private AddressTo1 address = new AddressTo1();
    private String phone1;
    private String phone2;
    private String fax;
    private String email;
    private String serviceLocationIdentifier;
    private String notes;
    private Date addDate;
    private Character status;

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public AddressTo1 getAddress() {
        return address;
    }

    public void setAddress(AddressTo1 address) {
        this.address = address;
    }

    public String getPhone1() {
        return phone1;
    }

    public void setPhone1(String phone1) {
        this.phone1 = phone1;
    }

    public String getPhone2() {
        return phone2;
    }

    public void setPhone2(String phone2) {
        this.phone2 = phone2;
    }

    public String getFax() {
        return fax;
    }

    public void setFax(String fax) {
        this.fax = fax;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getServiceLocationIdentifier() {
        return serviceLocationIdentifier;
    }

    public void setServiceLocationIdentifier(String serviceLocationIdentifier) {
        this.serviceLocationIdentifier = serviceLocationIdentifier;
    }

    public String getNotes() {
        return notes;
    }

    public void setNotes(String notes) {
        this.notes = notes;
    }

    public Date getAddDate() {
        return addDate;
    }

    public void setAddDate(Date addDate) {
        this.addDate = addDate;
    }

    public Character getStatus() {
        return status;
    }

    public void setStatus(Character status) {
        this.status = status;
    }
}
